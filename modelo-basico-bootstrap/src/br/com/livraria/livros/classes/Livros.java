package br.com.livraria.livros.classes;

public class Livros {
	
	
	public Integer cd_livro;
	public String nm_livro;
	public Integer n_ativo;
	public String data;
	
	
	public Integer getCd_livro() {
		return cd_livro;
	}
	public void setCd_livro(Integer cd_livro) {
		this.cd_livro = cd_livro;
	}
	public String getNm_livro() {
		return nm_livro;
	}
	public void setNm_livro(String nm_livro) {
		this.nm_livro = nm_livro;
	}
	public Integer getN_ativo() {
		return n_ativo;
	}
	public void setN_ativo(Integer n_ativo) {
		this.n_ativo = n_ativo;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}


}
